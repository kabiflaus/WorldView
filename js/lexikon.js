const KATEGORIEN = {
    geopolitik: "🌍 Geopolitik",
    finanzen: "💰 Finanzen",
    zusammenhaenge: "🔗 Zusammenhänge"
};

let aktivKategorie = 'alle';

function renderLex(filter = '') {
    const container = document.getElementById('lex-list');
    let html = '';

    Object.entries(LEXIKON).forEach(([key, eintraege]) => {
        if (aktivKategorie !== 'alle' && aktivKategorie !== key) return;

        const gefiltert = eintraege.filter(e =>
            e.begriff.toLowerCase().includes(filter.toLowerCase()) ||
            e.kurz.toLowerCase().includes(filter.toLowerCase()) ||
            e.erklaerung.toLowerCase().includes(filter.toLowerCase())
        );

        if (gefiltert.length === 0) return;

        html += `<div class="lex-kategorie-titel">${KATEGORIEN[key]}</div>`;
        html += gefiltert.map(e => `
            <div class="lex-item" onclick="toggleLex(this)">
                <div class="lex-top">
                    <span class="lex-begriff">${e.begriff}</span>
                    <span class="lex-kurz">${e.kurz}</span>
                    <span class="lex-arrow">▸</span>
                </div>
                <div class="lex-body">${e.erklaerung}</div>
            </div>
        `).join('');
    });

    container.innerHTML = html || '<div style="color:var(--muted);font-family:\'DM Mono\',monospace;font-size:12px;padding:16px">Keine Ergebnisse.</div>';
}

function toggleLex(el) {
    el.classList.toggle('open');
}

function filterLex(val) {
    renderLex(val);
}

function setKategorie(kat) {
    aktivKategorie = kat;
    document.querySelectorAll('.lex-filter-btn').forEach(b => b.classList.remove('active'));
    document.querySelector(`[data-kat="${kat}"]`).classList.add('active');
    renderLex(document.getElementById('lex-search').value);
}

renderLex();